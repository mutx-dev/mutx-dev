def _build_env_template(state: OnboardingState) -> str:
    """Provider-specific .env.template."""
    lines = ["# API Keys — fill in your keys before running",
             "# Never commit this file or share keys in chat", ""]

    provider_keys = {
        "openai": "OPENAI_API_KEY=sk-...",
        "anthropic": "ANTHROPIC_API_KEY=sk-ant-...",
        "google": "GOOGLE_API_KEY=AIza...",
        "local": "# LOCAL_MODEL_URL=http://127.0.0.1:1234/v1",
    }

    if state.provider:
        lines.append(provider_keys.get(state.provider, "# PROVIDER_KEY="))
    else:
        for v in provider_keys.values():
            lines.append(v)

    lines.append("")
    if state.channels:
        if "telegram" in state.channels:
            lines.append("TELEGRAM_BOT_TOKEN=")
        if "discord" in state.channels:
            lines.append("DISCORD_BOT_TOKEN=")
        if "slack" in state.channels:
            lines.append("SLACK_BOT_TOKEN=")
            lines.append("SLACK_SIGNING_SECRET=")
        if "whatsapp" in state.channels:
            lines.append("WHATSAPP_TOKEN=")

    return "\n".join(lines)


