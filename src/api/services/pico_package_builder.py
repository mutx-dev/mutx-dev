def _build_install_sh(state: OnboardingState) -> str:
    """OS-specific install script."""
    os_name = state.os or "linux"
    stack = state.stack or "hermes"

    lines = ["#!/usr/bin/env bash", "set -euo pipefail", "", "echo '=== PicoMUTX Setup ==='", ""]

    if stack == "hermes":
        if os_name == "macos":
            lines.append("# Install Hermes on macOS")
            lines.append("if ! command -v hermes &>/dev/null; then")
            lines.append("  curl -fsSL https://get.hermes.dev | bash")
            lines.append("fi")
            lines.append("")
            lines.append("hermes setup")
        elif os_name == "windows_wsl2":
            lines.append("# Install Hermes on WSL2")
            lines.append("if ! command -v hermes &>/dev/null; then")
            lines.append("  curl -fsSL https://get.hermes.dev | bash")
            lines.append("fi")
            lines.append("")
            lines.append("hermes setup")
        else:
            lines.append("# Install Hermes on Linux")
            lines.append("if ! command -v hermes &>/dev/null; then")
            lines.append("  curl -fsSL https://get.hermes.dev | bash")
            lines.append("fi")
            lines.append("")
            lines.append("hermes setup")
    elif stack == "openclaw":
        lines.append("# Install Node 24 (recommended)")
        if os_name == "macos":
            lines.append("brew install node@24 || true")
        else:
            lines.append("curl -fsSL https://deb.nodesource.com/setup_24.x | sudo -E bash -")
            lines.append("sudo apt-get install -y nodejs")
        lines.append("")
        lines.append("npm install -g openclaw")
        lines.append("openclaw onboard --install-daemon")
    elif stack == "nanoclaw":
        lines.append("# Ensure Docker is running")
        lines.append("docker info >/dev/null 2>&1 || { echo 'Docker must be running'; exit 1; }")
        lines.append("")
        lines.append("# Clone and setup")
        lines.append("git clone https://github.com/nanoclaw/nanoclaw.git")
        lines.append("cd nanoclaw")
        lines.append("# Run /setup inside Claude Code")
    elif stack == "picoclaw":
        if os_name == "macos":
            lines.append("# Download PicoClaw for macOS")
            lines.append("curl -fsSL https://picoclaw.dev/install.sh | bash")
        elif os_name == "linux":
            lines.append("# Download PicoClaw for Linux")
            lines.append("curl -fsSL https://picoclaw.dev/install.sh | bash")
        else:
            lines.append("# Download from https://picoclaw.dev")
        lines.append("")
        lines.append("picoclaw init")

    lines.append("")
    lines.append("echo 'Setup complete. Check README.md for next steps.'")

    return "\n".join(lines)


def _build_tailscale_setup(state: OnboardingState) -> str:
    """Tailscale setup script — included only when networking == tailscale."""
    return """#!/usr/bin/env bash
set -euo pipefail

echo '=== Tailscale Setup ==='

if ! command -v tailscale &>/dev/null; then
  echo 'Installing Tailscale...'
  curl -fsSL https://tailscale.com/install.sh | sh
fi

echo 'Authenticating with Tailscale...'
sudo tailscale up

echo 'Tailscale is active.'
echo '  Status: tailscale status'
echo '  Your IP: tailscale ip'
echo ''
echo 'To share a local service on your tailnet:'
echo '  tailscale serve --bg <port>'
echo ''
echo 'WARNING: tailscale funnel exposes services publicly. Do NOT use unless needed.'
"""


