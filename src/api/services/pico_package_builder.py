def build_onboarding_package(state: OnboardingState) -> tuple[bytes, str]:
    """Generate a ZIP file with real, stack-specific config files.

    Returns:
        (zip_bytes, filename) — filename is '{stack}-setup.zip'
    """
    stack = state.stack or "hermes"
    filename = f"{stack}-setup.zip"

    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        # Core config per stack
        if stack == "hermes":
            zf.writestr("config.yaml", _build_hermes_config(state))
            zf.writestr("AGENTS.md", _build_agents_md(state))
        elif stack == "openclaw":
            zf.writestr("openclaw.json", _build_openclaw_config(state))
        elif stack == "nanoclaw":
            zf.writestr("docker-compose.yml", _build_nanoclaw_compose(state))
        elif stack == "picoclaw":
            zf.writestr("config.json", _build_picoclaw_config(state))
            zf.writestr(".security.yml.template", "# Sensitive values go here\nprovider:\n  apiKey: \n")

        # Shared files
        zf.writestr(".env.template", _build_env_template(state))
        zf.writestr("install.sh", _build_install_sh(state))
        zf.writestr("README.md", _build_readme(state))

        # Channel configs
        for channel in (state.channels or []):
            ch_filename, ch_content = _build_channel_config(channel, state)
            zf.writestr(f"channels/{ch_filename}", ch_content)

        # Tailscale
        if state.networking == "tailscale":
            zf.writestr("tailscale-setup.sh", _build_tailscale_setup(state))

    return buf.getvalue(), filename
