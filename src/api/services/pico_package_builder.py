def _build_hermes_config(state: OnboardingState) -> str:
    """Real Hermes config.yaml."""
    provider_map = {
        "openai": "openai",
        "anthropic": "anthropic",
        "google": "google",
        "local": "lm-studio",
    }
    provider = provider_map.get(state.provider, "openai")
    model_default = {
        "openai": "gpt-5-mini",
        "anthropic": "claude-sonnet-4",
        "google": "gemini-2.5-pro",
        "local": "local-model",
    }
    model = model_default.get(state.provider, "gpt-5-mini")

    channels_yaml = ""
    if state.channels:
        channels_yaml = "\n".join(f"  - {ch}" for ch in state.channels)
    else:
        channels_yaml = "  # - telegram\n  # - discord\n  # - slack"

    return f"""# Hermes Agent Configuration
# Generated by PicoMUTX Builder — {datetime.now(timezone.utc).strftime("%Y-%m-%d")}

model:
  provider: {provider}
  name: {model}
  temperature: 0.7

toolsets:
  - web
  - file
  - terminal

channels:
{channels_yaml}

terminal:
  backend: local

memory:
  enabled: true
  max_entries: 1000
"""


